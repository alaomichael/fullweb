<?php 
 // While loop
 $num = 1;
 while ($num < 7) {
     echo $num . " is less than 7. <br/>";
     $num++; // This must always be added, else the code will loop forever!
 }
echo "<br/>";

// Do While loop
$num = 0;
do  {
echo $num . " is less than 8. <br/>";
    $num++; // This must always be added, else the code will loop forever!
} while ($num < 8) ;

echo "<br/>";

?>